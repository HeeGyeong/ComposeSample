/**
 * 📚 SnapshotFlow vs collectAsState 실전 예제 학습 가이드
 * 
 * 이 문서는 SnapshotFlowExampleUI와 SnapshotFlowExampleViewModel의 상세한 학습 가이드를 제공합니다.
 * 
 * =================================================================================================
 * 🎯 이 예제로 학습할 수 있는 내용
 * =================================================================================================
 * 
 * 1. collectAsState와 snapshotFlow의 올바른 사용 시나리오 구분
 * 2. collectAsStateWithLifecycle을 통한 라이프사이클 인식 상태 관리
 * 3. 고빈도 업데이트 데이터 처리 시 성능 고려사항
 * 4. ViewModel에서 라이프사이클 이벤트 처리하여 리소스 최적화
 * 5. 애니메이션 상태 보존과 재개 로직 구현
 * 
 * =================================================================================================
 * 🔍 핵심 개념
 * =================================================================================================
 * 
 * - collectAsState: Flow → Compose State 변환 (UI 업데이트용)
 * - snapshotFlow: Compose State → Flow 변환 (side-effect용)
 * - collectAsStateWithLifecycle: 백그라운드에서 자동 구독 해제
 * 
 * =================================================================================================
 * 💡 실무에서의 적용
 * =================================================================================================
 * 
 * - API 응답 데이터 → collectAsState
 * - 스크롤 위치 추적 → snapshotFlow
 * - 애니메이션/타이머 → collectAsStateWithLifecycle + 라이프사이클 관리
 * 
 * =================================================================================================
 * 🎬 ViewModel 구조 및 학습 포인트
 * =================================================================================================
 * 
 * 1️⃣ collectAsState 올바른 사용 예시: 저빈도 사용자 입력 데이터
 *    - 사용자가 버튼을 클릭할 때만 업데이트되는 데이터
 *    - UI에서 collectAsState()로 구독하여 화면에 표시
 *    - 성능 문제 없음: 업데이트 빈도가 낮음
 *    - 실무 예시: 사용자 프로필, 폼 입력값, API 응답
 * 
 * 2️⃣ collectAsStateWithLifecycle 올바른 사용 예시: 애니메이션 진행도
 *    - 지속적으로 업데이트되지만 라이프사이클 인식이 중요한 데이터
 *    - 백그라운드에서는 자동으로 구독 해제되어 리소스 절약
 *    - 포그라운드 복귀 시 자동으로 다시 구독
 *    - 실무 예시: 실시간 채팅, 위치 추적, 음악 재생 진행도, 타이머
 * 
 * 3️⃣ collectAsState 잘못된 사용 예시: 고빈도 업데이트 데이터
 *    - 매 100ms마다 업데이트되는 데이터를 collectAsState로 구독
 *    - 결과: 매번 recomposition 발생으로 성능 저하 유발
 *    - 올바른 해결책: 필요할 때만 샘플링하거나 throttling 적용
 *    - 실무에서 흔한 실수: 실시간 위치, WebSocket 스트림, 센서 데이터를 raw로 구독
 * 
 * 4️⃣ side-effect 결과 수집용: 로그 메시지들
 *    - snapshotFlow에서 발생한 이벤트들을 수집
 *    - UI에 표시하여 side-effect 동작 확인 가능
 *    - 실무에서는 Firebase Analytics, Crashlytics 등으로 대체
 * 
 * =================================================================================================
 * 🎨 UI 컴포넌트별 학습 가이드
 * =================================================================================================
 * 
 * 📋 CollectAsStateExampleCard - collectAsState 올바른 사용 예시
 * ────────────────────────────────────────────────────────────────────────────────────────────
 * 🎯 학습 목표:
 *    - ViewModel의 StateFlow를 UI State로 변환하는 기본 패턴
 *    - 저빈도 업데이트 데이터에 적합한 사용법 체험
 * 
 * 📊 성능 특성:
 *    - 사용자가 버튼을 클릭할 때만 recomposition 발생
 *    - 성능 문제 없음: 업데이트 빈도가 낮기 때문
 * 
 * 💼 실무 적용 시나리오:
 *    - 사용자 프로필 정보 표시
 *    - 폼 입력 값 바인딩
 *    - API 응답 결과 표시
 * 
 * 📋 CollectAsStateWithLifecycleCard - collectAsStateWithLifecycle 실전 활용
 * ────────────────────────────────────────────────────────────────────────────────────────────
 * 🎯 학습 목표:
 *    - 라이프사이클 인식하는 상태 구독의 중요성
 *    - 백그라운드에서 자동 일시정지/재개되는 동작 체험
 *    - ViewModel에서 라이프사이클 이벤트 처리하는 패턴
 * 
 * 🔋 메모리/배터리 최적화:
 *    - 백그라운드: 자동 구독 해제 → CPU 사용량 ↓
 *    - 포그라운드: 자동 재구독 → 자연스러운 UX
 *    - 애니메이션 상태 보존: 이전 진행도부터 재개
 * 
 * 💼 실무 적용 시나리오:
 *    - 실시간 채팅 메시지 수신
 *    - 위치 추적 앱의 GPS 업데이트
 *    - 음악 플레이어의 재생 진행도
 *    - 타이머/스톱워치 앱
 * 
 * 📋 SnapshotFlowExampleCard - snapshotFlow 실전 활용
 * ────────────────────────────────────────────────────────────────────────────────────────────
 * 🎯 학습 목표:
 *    - Compose State를 Flow로 변환하여 side-effect 처리
 *    - UI 상태 변화를 추적하는 정석 패턴
 *    - distinctUntilChanged()로 중복 이벤트 필터링
 * 
 * 📊 동작 원리:
 *    - LazyList의 스크롤 상태(Compose State) → Flow로 변환
 *    - 스크롤 위치 변경 시에만 side-effect 실행
 *    - UI 업데이트용이 아닌 로깅/분석용으로 활용
 * 
 * 💼 실무 적용 시나리오:
 *    - 스크롤 위치 기반 분석 (어느 컨텐츠를 많이 봤는지)
 *    - 제스처 추적 (스와이프, 드래그 패턴)
 *    - 사용자 행동 로깅 (페이지 체류 시간)
 *    - A/B 테스트 데이터 수집
 * 
 * 📋 WrongUsageExampleCard - collectAsState 잘못된 사용 예시 (⚠️ 안티패턴)
 * ────────────────────────────────────────────────────────────────────────────────────────────
 * 🎯 학습 목표:
 *    - 고빈도 업데이트 데이터에 collectAsState 사용 시 성능 문제 체험
 *    - recomposition 과다 발생으로 인한 UI 성능 저하 관찰
 *    - 실무에서 피해야 할 패턴을 명확히 인지
 * 
 * 🚨 문제점:
 *    - 매 100ms마다 recomposition 발생 (초당 10회!)
 *    - CPU 사용률 증가, 배터리 소모 증가
 *    - 다른 UI 컴포넌트까지 영향을 미칠 수 있음
 *    - 사용자 입력 반응성 저하 가능
 * 
 * 💡 올바른 해결 방법:
 *    - throttling/sampling으로 업데이트 빈도 제한
 *    - 필요할 때만 일시적으로 구독
 *    - 디바운싱 적용으로 연속 업데이트 방지
 *    - derivedStateOf로 계산 최적화
 * 
 * 🔍 실무에서 흔한 실수:
 *    - 실시간 위치 데이터를 그대로 구독
 *    - WebSocket 스트림을 직접 collectAsState
 *    - 센서 데이터(가속도계, 자이로스코프)를 raw로 구독
 * 
 * =================================================================================================
 * 🔧 라이프사이클 관리 및 애니메이션 로직
 * =================================================================================================
 * 
 * 🎬 애니메이션 시작/재개 로직:
 * ────────────────────────────────────────────────────────────────────────────────────────────
 * 핵심 로직:
 * 1. 중복 실행 방지: 이미 실행 중인 Job이 있으면 리턴
 * 2. 상태 기반 재개: 현재 진행도부터 이어서 시작
 * 3. 사이클 관리: 100% 완료 후 2초 대기 → 0%부터 새 사이클
 * 
 * 실무 적용 예시: 타이머, 프로그레스바, 실시간 차트 애니메이션
 * 
 * 📱 라이프사이클 콜백:
 * ────────────────────────────────────────────────────────────────────────────────────────────
 * onResume: 포그라운드 복귀 시 이전 진행도부터 애니메이션 재개
 *          🎯 핵심: 사용자가 앱을 다시 열었을 때 자연스러운 UX 제공
 * 
 * onPause:  백그라운드 진입 시 현재 진행도 보존하고 애니메이션 일시정지
 *          🎯 목적: CPU 사용량 절약, 배터리 수명 연장
 * 
 * ⏸️ 애니메이션 일시정지 로직:
 * ────────────────────────────────────────────────────────────────────────────────────────────
 * 중요한 점:
 * - Job을 cancel()하지만 currentAnimationStep은 보존
 * - 따라서 다음 startAnimation() 호출 시 이전 위치부터 재개 가능
 * - currentAnimationStep과 isInWaitingPeriod는 보존하여 재개 시 이전 상태부터 계속 진행
