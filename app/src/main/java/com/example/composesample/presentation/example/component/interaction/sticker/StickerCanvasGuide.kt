/**
 * 📚 Sticker Canvas (Gestures, Physics, Die-Cut) 학습 가이드
 *
 * 출처: https://aditlal.dev/building-stickerexplode-part-1-gestures-physics-and-making-stickers-feel-real/
 *
 * 이 문서는 StickerExplode 앱의 Part 1에 기반하여
 * 제스처 시스템, 스프링 물리, 필오프 애니메이션, 다이컷 렌더링을 다룹니다.
 *
 * =================================================================================================
 * 🎯 이 예제로 학습할 수 있는 내용
 * =================================================================================================
 *
 * 1. 복합 제스처 (Drag + Pinch + Rotate) 동시 처리
 * 2. Spring 물리 애니메이션 적용 (spring(), Spring.DampingRatioMediumBouncy)
 * 3. 필오프(Peel-off) 효과: 스티커를 잡으면 들어올리는 듯한 그림자 + 스케일 변화
 * 4. Die-cut 렌더링: 실제 비닐 스티커 같은 흰색 테두리
 * 5. Z-ordering: 단조 증가 카운터로 탭 시 최상위 배치
 * 6. 더블 탭 바운시 줌 (2x 확대/복원)
 * 7. 스티커 데이터 모델 설계 패턴
 *
 * =================================================================================================
 * 🔍 핵심 개념: 스티커 데이터 모델
 * =================================================================================================
 *
 * ```kotlin
 * data class StickerItem(
 *     val id: Int,
 *     val type: StickerType,           // Emoji, Text, Shape, Icon
 *     val initialFractionX: Float,     // 0..1 상대 좌표
 *     val initialFractionY: Float,
 *     val rotation: Float = 0f,
 *     val offsetX: Float = Float.NaN,  // 드래그 후 픽셀 좌표
 *     val offsetY: Float = Float.NaN,
 *     val pinchScale: Float = 1f,      // 0.5x ~ 3x
 *     val zIndex: Float = 0f,          // 단조 증가 카운터
 * )
 * ```
 *
 * - 새 스티커는 비율 좌표(0..1)로 시작 → 어떤 화면에서든 동작
 * - 드래그 시 픽셀 좌표로 전환 (Float.NaN 체크)
 * - zIndex는 탭할 때마다 증가하여 최상위 배치
 *
 * =================================================================================================
 * 📱 제스처 시스템 상세
 * =================================================================================================
 *
 * 1️⃣ 드래그 (한 손가락):
 *    - detectTransformGestures로 pan offset 누적
 *    - 캔버스 영역 내 clamp 처리
 *
 * 2️⃣ 핀치 줌 (두 손가락):
 *    - zoom factor를 0.5x ~ 3.0x로 제한
 *    - detectTransformGestures의 zoom 파라미터 활용
 *
 * 3️⃣ 회전 (두 손가락):
 *    - detectTransformGestures의 rotation 파라미터
 *    - graphicsLayer의 rotationZ에 적용
 *
 * 4️⃣ 탭 (한 손가락):
 *    - detectTapGestures의 onTap
 *    - zIndex 증가 → 최상위 배치
 *
 * 5️⃣ 더블 탭:
 *    - detectTapGestures의 onDoubleTap
 *    - 바운시 스프링으로 2x ↔ 1x 토글
 *
 * =================================================================================================
 * 🎬 Spring 물리 & 필오프 애니메이션
 * =================================================================================================
 *
 * Spring 물리:
 * - spring(dampingRatio = Spring.DampingRatioMediumBouncy)
 * - 스티커 놓을 때, 더블 탭 줌, 새 스티커 등장 시 적용
 * - 자연스러운 바운스 효과로 물리적 느낌
 *
 * 필오프 효과:
 * - 스티커를 잡으면(드래그 시작):
 *   • scale이 1.0 → 1.1로 증가 (들어올리는 효과)
 *   • shadow elevation이 4dp → 16dp로 증가
 *   • alpha가 약간 감소 (0.95)
 * - 놓으면 spring 애니메이션으로 원래 상태 복귀
 *
 * =================================================================================================
 * 🎨 Die-Cut 렌더링
 * =================================================================================================
 *
 * 실제 비닐 스티커처럼 흰색 테두리를 그리는 기법:
 * 1. 외부 Box에 흰색 배경 + RoundedCornerShape
 * 2. 내부 콘텐츠는 패딩으로 인셋
 * 3. 그림자(elevation)로 입체감
 * 4. clip(shape)으로 콘텐츠 마스킹
 *
 * =================================================================================================
 * 🔧 Z-Ordering 전략
 * =================================================================================================
 *
 * - zIndexCounter: Float를 단조 증가
 * - 탭 시 다음 값 부여 → 최상위 배치
 * - Compose의 Modifier.zIndex()로 레이어 순서 제어
 * - 새 스티커 추가 시에도 최상위 zIndex 부여
 *
 * =================================================================================================
 * 🚀 실무 적용 가이드
 * =================================================================================================
 *
 * ✅ 적용 가능한 시나리오:
 *    - 포토 에디터의 스티커/텍스트 배치
 *    - 화이트보드 앱의 오브젝트 조작
 *    - 프레젠테이션 도구의 요소 배치
 *    - 게임 UI의 인벤토리 드래그
 *
 * ✅ 성능 최적화 팁:
 *    - graphicsLayer로 하드웨어 가속 활용
 *    - remember로 제스처 상태 캐싱
 *    - mutableStateListOf로 효율적 리스트 관리
 *
 * ❌ 주의사항:
 *    - 제스처 충돌 방지 (pointerInput key 설정)
 *    - 스케일/회전 제한 범위 설정 필수
 *    - 메모리 관리 (스티커 수 제한 권장)
