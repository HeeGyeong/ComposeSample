 * 🎯 조건부 애니메이션:
 * ```kotlin
 * @Composable
 * fun ConditionalAnimatedLayout(
 *     enableAnimation: Boolean = true,
 *     content: @Composable LookaheadScope.() -> Unit
 * ) {
 *     if (enableAnimation) {
 *         LookaheadScope {
 *             content()
 *         }
 *     } else {
 *         // 애니메이션 없이 즉시 변경
 *         content(NoOpLookaheadScope)
 *     }
 * }
 * ```
 * 
 * 🎯 중첩된 LookaheadScope:
 * ```kotlin
 * LookaheadScope { // 전체 화면 레이아웃
 *     Column {
 *         LookaheadScope { // 헤더 영역 
 *             HeaderContent(modifier = Modifier.animateBounds(this))
 *         }
 *         LookaheadScope { // 콘텐츠 영역
 *             MainContent(modifier = Modifier.animateBounds(this))
 *         }
 *     }
 * }
 * ```
 * 
 * 🎯 커스텀 boundsTransform:
 * ```kotlin
 * fun createCustomBoundsTransform(
 *     duration: Int = 500,
 *     easing: Easing = FastOutSlowInEasing
 * ): (Rect, Rect) -> FiniteAnimationSpec<Rect> {
 *     return { _, _ ->
 *         tween(
 *             durationMillis = duration,
 *             easing = easing
 *         )
 *     }
 * }
 * ```
