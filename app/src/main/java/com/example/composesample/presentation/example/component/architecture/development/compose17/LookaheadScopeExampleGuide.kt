 * 이 문서는 LookaheadScopeExampleUI의 상세한 학습 가이드를 제공합니다.
 * 
 * =================================================================================================
 * 🎯 이 예제로 학습할 수 있는 내용
 * =================================================================================================
 * 
 * 1. LookaheadScope의 작동 원리와 레이아웃 예측 시스템
 * 2. animateBounds() 모디파이어를 통한 자동 크기/위치 애니메이션
 * 3. boundsTransform으로 커스텀 애니메이션 스펙 적용
 * 4. 복잡한 레이아웃 변경 시 자연스러운 전환 효과
 * 5. 성능 최적화된 레이아웃 애니메이션 구현
 * 
 * =================================================================================================
 * 🔍 핵심 개념: LookaheadScope와 Layout Prediction
 * =================================================================================================
 * 
 * 📝 LookaheadScope란?
 * - Compose의 레이아웃 변경을 미리 계산하여 부드러운 애니메이션을 제공하는 시스템
 * - 실제 레이아웃 적용 전에 "미리보기" 단계를 거쳐 변경사항을 예측
 * - animateBounds()와 함께 사용하여 크기, 위치 변경을 자동으로 애니메이션화
 * 
 * 🎯 동작 원리:
 * 1. Lookahead Pass: 미래 레이아웃 상태를 미리 계산
 * 2. Measurement Pass: 현재 레이아웃을 실제로 측정
 * 3. Animation: 현재 상태에서 미래 상태로의 부드러운 전환
 * 4. Placement: 최종 위치에 요소 배치
 * 
 * 🚀 장점:
 * - 개발자가 수동으로 애니메이션을 구현할 필요 없음
 * - 복잡한 레이아웃 변경도 자동으로 처리
 * - 성능 최적화된 GPU 가속 애니메이션
 * - 다양한 애니메이션 스펙 적용 가능
 * 
 * =================================================================================================
 * 🎨 UI 컴포넌트별 상세 가이드
 * =================================================================================================
 * 
 * 📋 SizeChangeAnimationExample - 크기 변경 애니메이션
 * ────────────────────────────────────────────────────────────────────────────────────────────
 * 🎯 구현 패턴:
 *    - LookaheadScope로 전체 영역 감싸기
 *    - animateBounds()로 크기 변경 추적
 *    - 조건부 크기 변경으로 애니메이션 트리거
 * 
 * 💡 핵심 구현:
 * ```kotlin
 * LookaheadScope {
 *     Box(
 *         modifier = Modifier
 *             .width(if (expanded) 300.dp else 150.dp)
 *             .height(if (expanded) 150.dp else 75.dp)
 *             .animateBounds(
 *                 lookaheadScope = this@LookaheadScope,
 *                 boundsTransform = { _, _ -> 
 *                     spring(dampingRatio = 0.8f, stiffness = 400f)
 *                 }
 *             )
 *     )
 * }
 * ```
 * 
 * 🔧 boundsTransform 활용:
 * - spring(): 자연스러운 탄성 애니메이션
 * - tween(): 일정한 속도의 선형 애니메이션  
 * - keyframes(): 복잡한 키프레임 애니메이션
 * - 커스텀 AnimationSpec으로 고유한 효과 생성
 * 
 * 📋 PositionChangeAnimationExample - 위치 변경 애니메이션
 * ────────────────────────────────────────────────────────────────────────────────────────────
 * 🎯 고급 기능:
 *    - offset() modifier로 위치 변경
 *    - 자동으로 이전 위치에서 새 위치로 애니메이션
 *    - 복잡한 레이아웃 내에서도 정확한 위치 추적
 * 
 * 💡 위치 애니메이션 패턴:
 * ```kotlin
 * Box(
 *     modifier = Modifier
 *         .offset(x = if (isRight) 200.dp else 0.dp)
 *         .animateBounds(
 *             lookaheadScope = this@LookaheadScope,
 *             boundsTransform = { _, _ -> 
 *                 spring(dampingRatio = 0.7f, stiffness = 300f)
 *             }
 *         )
 * )
 * ```
 * 
 * 🔧 최적화 팁:
 * - 적절한 dampingRatio로 과도한 진동 방지
 * - stiffness 조절로 애니메이션 속도 제어
 * - visibilityThreshold로 불필요한 미세 움직임 제거
 * 
 * 📋 ComplexAnimationExample - 복합 애니메이션
 * ────────────────────────────────────────────────────────────────────────────────────────────
 * 🎯 다중 속성 애니메이션:
 *    - 크기 + 위치 + 정렬이 동시에 변경
 *    - 각 상태별로 다른 스타일 적용
 *    - 하나의 animateBounds()로 모든 변경사항 처리
 * 
 * 💡 복합 레이아웃 변경:
 * ```kotlin
 * Row(
 *     horizontalArrangement = when (state) {
 *         0 -> Arrangement.Start
 *         1 -> Arrangement.Center  
 *         else -> Arrangement.End
 *     },
 *     verticalAlignment = when (state) {
 *         0 -> Alignment.Top
 *         1 -> Alignment.CenterVertically
 *         else -> Alignment.Bottom
 *     }
 * ) {
 *     Box(
 *         modifier = Modifier
 *             .size(dynamicSize)
 *             .animateBounds(lookaheadScope = this@LookaheadScope)
 *     )
 * }
 * ```
 * 
 * =================================================================================================
 * 🚀 실무 적용 시나리오
 * =================================================================================================
 * 
 * ✅ 반응형 레이아웃:
 *    - 화면 크기 변경 시 자동 레이아웃 조정
 *    - 가로/세로 모드 전환 애니메이션
 *    - 다양한 화면 해상도 대응
 * 
 * ✅ 동적 콘텐츠:
 *    - 검색 결과에 따른 리스트 레이아웃 변경
 *    - 필터링으로 인한 아이템 재배치
 *    - 콘텐츠 로딩 상태에 따른 UI 변화
 * 
 * ✅ 인터랙티브 UI:
 *    - 탭 전환 시 콘텐츠 영역 크기 변경
 *    - 사이드바 열기/닫기 애니메이션
 *    - 카드 확장/축소 효과
 * 
 * ✅ 게임 UI:
 *    - 인벤토리 슬롯 재배치
