 * 🔧 NestedScrollConnection 상세 구현 로직
 * =================================================================================================
 * 
 * 🎬 onPreScroll 구현 전략:
 * ────────────────────────────────────────────────────────────────────────────────────────────
 * 목적: 위로 스크롤 시 툴바 숨기기 우선 처리
 * 
 * 동작 로직:
 * 1. 스크롤 방향 판단 (delta < 0 = 위로 스크롤)
 * 2. 위로 스크롤 시에만 처리 (30% 소비)
 * 3. 툴바 오프셋 업데이트 (0 ~ -toolbarHeightPx 범위)
 * 4. 소비한 양만큼 Offset 반환
 * 5. 나머지 70%는 LazyColumn으로 전달
 * 
 * 핵심 코드:
 * ```kotlin
 * val preConsume = delta * 0.3f  // 30% 소비
 * val newOffset = current + preConsume
 * toolbarOffset = newOffset.coerceIn(-max, 0f)
 * return Offset(0f, preConsume)
 * ```
 * 
 * 🎬 onPostScroll 구현 전략:
 * ────────────────────────────────────────────────────────────────────────────────────────────
 * 목적: 아래로 스크롤 시 LazyColumn이 더 이상 스크롤할 수 없을 때 툴바 보이기
 * 
 * 동작 로직:
 * 1. available.y > 0 (아래로 스크롤 && LazyColumn 처리 불가)
 * 2. 남은 스크롤 양 전체를 툴바 보이기에 사용
 * 3. 툴바 오프셋 업데이트 (0에 가까워짐)
 * 4. 실제 소비한 양만 반환
 * 
 * 핵심 코드:
 * ```kotlin
 * if (available.y > 0) {  // LazyColumn이 더 이상 스크롤 불가
 *     val newOffset = current + available.y
 *     toolbarOffset = newOffset.coerceIn(-max, 0f)
 *     return Offset(0f, available.y)
 * }
 * ```
 * 
 * 🎬 Fling 처리 전략:
 * ────────────────────────────────────────────────────────────────────────────────────────────
 * onPreFling:
 * - 고속 스크롤 시 속도의 일부만 소비 (30%)
 * - 남은 속도는 LazyColumn의 자연스러운 플링에 활용
 * - 임계값(1000) 이상일 때만 처리
 * 
 * onPostFling:
 * - LazyColumn 플링 완료 후 추가 처리
 * - 현재 구현에서는 별도 처리 없음 (Velocity.Zero 반환)
 * - 필요시 추가 애니메이션 효과 구현 가능
