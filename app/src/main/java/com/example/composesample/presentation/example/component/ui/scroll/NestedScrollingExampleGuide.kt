 * =================================================================================================
 * 🔍 핵심 개념: NestedScrollConnection 함수들
 * =================================================================================================
 * 
 * 📝 스크롤 처리 순서: Pre → Child → Post
 * 
 * 1️⃣ onPreScroll (자식 처리 전)
 *    - 자식이 스크롤을 처리하기 전에 부모가 먼저 처리
 *    - 용도: 헤더 숨기기, 우선 처리가 필요한 UI 효과
 *    - 반환값: 소비한 스크롤 양 (Offset)
 * 
 * 2️⃣ Child Scroll (자식 처리)
 *    - LazyColumn, LazyRow 등 자식 컴포넌트의 스크롤 처리
 *    - 자동으로 처리되며 개발자가 직접 구현하지 않음
 * 
 * 3️⃣ onPostScroll (자식 처리 후)
 *    - 자식이 더 이상 스크롤할 수 없을 때 부모가 처리
 *    - 용도: 헤더 보이기, Over-scroll 효과, 추가 스크롤 처리
 *    - 매개변수: consumed (자식이 소비한 양), available (남은 양)
 * 
 * 4️⃣ onPreFling / onPostFling (플링 처리)
 *    - 빠른 스크롤(플링) 시 속도 제어
 *    - 용도: 플링 속도 조절, 스무스 애니메이션 효과
 *    - 매개변수/반환값: Velocity (속도)
