 * 2. 동적 리스트 포커스 관리
 *    - 중간 아이템에 포커스 → 제거 → 인접 아이템으로 이동 확인
 * 
 * 3. 복잡한 중첩 시나리오
 *    - 여러 레벨의 포커스 복원이 올바르게 동작하는지 확인
 * 
 * 🔧 디버깅 팁:
 * ```kotlin
 * val focusManager = LocalFocusManager.current
