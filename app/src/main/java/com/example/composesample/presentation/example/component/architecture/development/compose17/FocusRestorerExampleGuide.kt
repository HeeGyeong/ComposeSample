 * ⚡ 렌더링 성능:
 *    - 포커스 복원이 recomposition을 유발할 수 있음
 *    - remember()로 상태 최적화
 *    - key 사용으로 안정적인 아이템 추적
 * 
 * ⚡ 사용자 경험:
 *    - 예측 가능한 포커스 이동 패턴 유지
 *    - 너무 빈번한 포커스 변경 방지
 *    - 시각적 포커스 표시 명확화
