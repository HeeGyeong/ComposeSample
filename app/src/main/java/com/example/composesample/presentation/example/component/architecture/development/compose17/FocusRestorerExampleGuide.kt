 * 4. 접근성을 고려한 포커스 흐름 설계
 * 5. 사용자 경험 개선을 위한 포커스 전략
 * 
 * =================================================================================================
 * 🔍 핵심 개념: focusRestorer() 모디파이어
 * =================================================================================================
 * 
 * 📝 Focus Restoration이란?
 * - 사용자가 다이얼로그, 팝업, 메뉴 등을 연 후 닫았을 때
 * - 이전에 포커스되어 있던 UI 요소로 자동으로 포커스를 복원하는 기능
 * - 키보드 네비게이션과 접근성에서 매우 중요한 UX 패턴
 * 
 * 🎯 focusRestorer() 동작 원리:
 * 1. 포커스 상태 추적: 현재 포커스된 요소를 내부적으로 기억
 * 2. 포커스 손실 감지: 다이얼로그 등으로 포커스가 이동했을 때 감지
 * 3. 자동 복원: 다이얼로그가 닫히면 이전 포커스 위치로 자동 복원
 * 4. 지능형 대체: 이전 요소가 사라진 경우 가장 적절한 대안 선택
 * 
 * =================================================================================================
 * 🎨 UI 컴포넌트별 상세 가이드
 * =================================================================================================
 * 
 * 📋 BasicFocusRestorerExample - 기본 포커스 복원
 * ────────────────────────────────────────────────────────────────────────────────────────────
 * 🎯 구현 패턴:
 *    - Column에 focusRestorer() 적용
 *    - 내부의 모든 TextField들이 포커스 복원 대상이 됨
 *    - AlertDialog 열기/닫기 시 자동 포커스 복원
 * 
 * 💡 핵심 코드:
 * ```kotlin
 * Column(modifier = Modifier.focusRestorer()) {
 *     TextField(...) // 포커스 복원 적용
 *     TextField(...) // 포커스 복원 적용
 * }
 * ```
 * 
 * 🔧 동작 시나리오:
 * 1. 사용자가 두 번째 TextField에 포커스
 * 2. "다이얼로그 열기" 버튼 클릭
 * 3. AlertDialog가 열리면서 포커스 이동
 * 4. "확인" 버튼으로 다이얼로그 닫기
 * 5. 자동으로 두 번째 TextField로 포커스 복원
 * 
 * 📋 LazyListFocusRestorerExample - 동적 리스트 포커스 복원
 * ────────────────────────────────────────────────────────────────────────────────────────────
 * 🎯 고급 기능:
 *    - LazyRow에 focusRestorer() 적용
 *    - 아이템 제거 시 지능형 포커스 재배치
 *    - key를 통한 안정적인 아이템 추적
 * 
 * 💡 핵심 구현:
 * ```kotlin
 * LazyRow(
 *     modifier = Modifier.focusRestorer(),
 *     key = { _, item -> item } // 안정적인 키 제공
 * ) {
 *     itemsIndexed(items, key = { _, item -> item }) { ... }
 * }
 * ```
 * 
 * 🔧 복원 알고리즘:
 * 1. 포커스된 아이템 제거 시 가장 가까운 아이템으로 이동
 * 2. 마지막 아이템 제거 시 이전 아이템으로 이동
 * 3. 첫 번째 아이템 제거 시 다음 아이템으로 이동
 * 4. 모든 아이템 제거 시 부모 컨테이너로 포커스 이동
 * 
 * =================================================================================================
 * 🚀 실무 적용 시나리오
 * =================================================================================================
 * 
 * ✅ 폼 입력 화면:
 *    - 여러 입력 필드가 있는 회원가입/로그인 폼
 *    - 에러 다이얼로그 후 해당 필드로 포커스 복원
 *    - 유효성 검사 팝업 후 포커스 유지
 * 
 * ✅ 설정 화면:
 *    - 설정 항목들 간의 포커스 이동
 *    - 확인 다이얼로그 후 이전 설정으로 복원
 *    - 도움말 팝업 후 포커스 유지
 * 
 * ✅ 검색 결과 화면:
 *    - 검색 결과 리스트에서 아이템 포커스
 *    - 상세 다이얼로그 후 리스트 포커스 복원
 *    - 필터링 후 적절한 아이템으로 포커스 이동
 * 
 * ✅ 채팅 앱:
 *    - 메시지 리스트에서 포커스 관리
 *    - 이미지 뷰어 후 메시지로 포커스 복원
 *    - 이모지 선택 후 입력 필드로 포커스 복원
 * 
 * =================================================================================================
 * 🔧 고급 활용 패턴
 * =================================================================================================
 * 
 * 🎯 중첩된 focusRestorer:
 * ```kotlin
 * Column(modifier = Modifier.focusRestorer()) { // 최상위 복원
 *     LazyColumn(modifier = Modifier.focusRestorer()) { // 리스트 레벨 복원
 *         items(list) { item ->
 *             Row(modifier = Modifier.focusRestorer()) { // 아이템 레벨 복원
 *                 // 세밀한 포커스 제어
 *             }
 *         }
 *     }
 * }
 * ```
 * 
 * 🎯 조건부 포커스 복원:
 * ```kotlin
 * val shouldRestoreFocus = remember { mutableStateOf(true) }
 * Column(
 *     modifier = if (shouldRestoreFocus.value) {
 *         Modifier.focusRestorer()
 *     } else {
 *         Modifier
 *     }
 * )
 * ```
 * 
 * 🎯 커스텀 포커스 전략:
 * ```kotlin
 * val focusRequester = remember { FocusRequester() }
 * LaunchedEffect(dialogClosed) {
 *     if (dialogClosed) {
 *         focusRequester.requestFocus() // 수동 포커스 제어
 *     }
 * }
 * ```
