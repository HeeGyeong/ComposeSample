 * ⌨️ 키보드 네비게이션:
 *    - Tab 키로 논리적 포커스 이동
 *    - Escape 키로 다이얼로그 닫기 + 포커스 복원
 *    - Arrow 키로 리스트 내 포커스 이동
 * 
 * 🎯 포커스 시각화:
 *    - 명확한 포커스 인디케이터
 *    - 고대비 포커스 테두리
 *    - 색상에만 의존하지 않는 표시
