 * =================================================================================================
 * 🎓 접근성(Accessibility) 고려사항
 * =================================================================================================
 * 
 * 🦽 스크린 리더 지원:
 *    - 포커스 복원 시 적절한 음성 안내
 *    - contentDescription으로 컨텍스트 제공
 *    - 의미 있는 포커스 순서 유지
